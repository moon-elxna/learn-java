package übung05;

public class Variablenwerte {
	
	public static void main(String[] args) {
		
		int k; 
		float f = 1.5f; 
		float g; 
		
		k = 10 * (int) f; 
		System.out.println("Variable k: " + k);
		//Vermutung: 10
		
		g = 10 * f; 
		System.out.println("Variable g: " + g);
		//Vermutung: 15
		
	}

}
